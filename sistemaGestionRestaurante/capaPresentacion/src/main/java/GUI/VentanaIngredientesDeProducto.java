    private void cargarDatos(){
        if(!ventana.ingredientesDeProducto.isEmpty()){
            jLabelNombreProducto.setText(ventana.ingredientesDeProducto.get(0).getProducto().getNombre());
        }
        DefaultTableModel model = (DefaultTableModel) jTableIngredientesProducto.getModel();
        model.setRowCount(0);
        for (IngredienteProducto ingrediente : ventana.ingredientesDeProducto) {
            model.addRow(new Object[]{
                ingrediente.getIngrediente().getNombre(),
                ingrediente.getCantidad(),
                ingrediente.getIngrediente().getUnidadMedida()
            });
        }
    }
    
