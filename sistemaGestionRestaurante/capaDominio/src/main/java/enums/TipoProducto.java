package enums;

/**
 * Enumeración que representa los distintos tipos de productos disponibles en el sistema.
 * Puede ser utilizada para clasificar productos en platillos, bebidas o postres.
 * 
 * @author janot
 */
public enum TipoProducto {
    /** Producto que corresponde a un platillo principal o secundario. */
    PLATILLO, 
    
    /** Producto que corresponde a una bebida, ya sea fría o caliente. */
    BEBIDA, 
    
    /** Producto que corresponde a un postre o dulce. */
    POSTRE
}
